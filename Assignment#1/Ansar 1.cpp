#include<iostream>
using namespace std;
int main()
{
	float num1,num2,multi,div,add,sub;
	num1=20;
	num2=10;
	add=num1+num2;
	sub=num1-num2;
	multi=num1*num2;
	div=num1/num2;
	cout<<"Sum: "<<add<<endl;
	cout<<"Subtraction: "<<sub<<endl;
	cout<<"Multiplication: "<<multi<<endl;
	cout<<"Division: "<<div;	
}




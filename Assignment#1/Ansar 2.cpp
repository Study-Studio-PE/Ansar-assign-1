#include<iostream>
using namespace std;
int main()
{
	int age,hour,min,sec;
	age=20;
	cout<<"Age= 20"<<endl;
	hour=20*24*365;
	min=20*24*365*60;
	sec=20*365*24*60*60;
	cout<<"Hours= "<<hour<<endl;
	cout<<"Minutes= "<<min<<endl;
	cout<<"Seconds= "<<sec<<endl;	
}




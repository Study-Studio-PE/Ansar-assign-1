// Step 4: Multiply age with 24 and 365 assign to hour
// Step 5: Multiply age with 24, 365 and 60 assign to min
// Step 6: Multiply age with 24, 365, 60 and 60 assign to sec
// Step 7: Print the values of hour,min, and sec variables
// Step 8: End

